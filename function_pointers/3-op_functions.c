#include "3-calc.h"


/**
 * op_add - adds values
 * @a: first num
 * @b: second num
 * Return: result
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *  * op_sub - sub values
 *   * @a: first num
 *    * @b: second num
 *     * Return: result
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *  * op_mul - multiplies values
 *   * @a: first num
 *    * @b: second num
 *     * Return: result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *  * op_div - div values
 *   * @a: first num
 *    * @b: second num
 *     * Return: result
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *  * op_mod - applies modulus to values
 *   * @a: first num
 *    * @b: second num
 *     * Return: result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
